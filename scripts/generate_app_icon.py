def build_svg() -> str:
    """Compose the standalone SVG string."""
    paths_xml = "\n  ".join(
        f'<path fill="{color}" d="{d}"/>' for color, d in PATHS
    )
    return f"""<?xml version="1.0" encoding="UTF-8"?>
<svg xmlns="http://www.w3.org/2000/svg" viewBox="{VIEW_BOX}">
  {paths_xml}
</svg>
"""


def render(svg_string: str, size: int) -> Image.Image:
    """Rasterize the SVG at `size` x `size` pixels."""
    png_bytes = resvg_py.svg_to_bytes(
        svg_string=svg_string,
        width=size,
        height=size,
    )
    if isinstance(png_bytes, list):
        png_bytes = bytes(png_bytes)
    return Image.open(io.BytesIO(png_bytes)).convert("RGBA")


def main():
    print("Generating app icon from in-GUI MICA brand SVG...")
    svg_string = build_svg()

    # Pre-render every size we'll need so the same crisp raster is reused
    # across all icon container formats.
    sizes = sorted({16, 24, 32, 48, 64, 96, 128, 256, 512, 1024})
    renders = {s: render(svg_string, s) for s in sizes}
    print(f"  rendered {len(renders)} sizes ({', '.join(map(str, sizes))})")

    # JavaFX runtime icon + Linux launcher PNG: identical 1024x1024 PNG.
    renders[1024].save(ICON_PNG, "PNG")
    print(f"  wrote {os.path.relpath(ICON_PNG, PROJECT_ROOT)} (1024x1024)")
    renders[1024].save(ICON_LINUX, "PNG")
    print(f"  wrote {os.path.relpath(ICON_LINUX, PROJECT_ROOT)} (1024x1024)")

    # No bootstrapper logo output — the Burn bootstrapper theme intentionally
    # has no page-logo <Image> control because WixStdBA's bitmap rendering
    # under Windows DPI virtualization clips the image at HiDPI scales.

    # Windows .ico: PIL packs the multi-resolution container itself.
    ico_sizes = [(16, 16), (24, 24), (32, 32), (48, 48), (64, 64),
                 (128, 128), (256, 256)]
    renders[256].save(ICON_WINDOWS, "ICO", sizes=ico_sizes,
                      append_images=[renders[s[0]] for s in ico_sizes[:-1]])
    print(f"  wrote {os.path.relpath(ICON_WINDOWS, PROJECT_ROOT)} (multi-res ICO)")

    # macOS .icns: PIL packs the icon family; supply explicit sizes so each
    # entry uses a freshly rendered raster instead of resampling 1024.
    icns_sizes = [(16, 16), (32, 32), (64, 64), (128, 128),
                  (256, 256), (512, 512), (1024, 1024)]
    renders[1024].save(ICON_MAC, "ICNS", sizes=icns_sizes,
                       append_images=[renders[s[0]] for s in icns_sizes[:-1]])
    print(f"  wrote {os.path.relpath(ICON_MAC, PROJECT_ROOT)} (multi-res ICNS)")

    print("Done. Rebuild installers to pick up the new icon.")


if __name__ == "__main__":
    main()
